void f_print(char* dat)
{
	uint8_t cnt=0,length=strlen((const char*)dat);
	char buffer[14];
	memset(buffer,0,sizeof(buffer));
	if(length<=14)
	{
		memset(buffer,' ',sizeof(buffer));
		for(cnt=0;cnt<length;cnt++)
			buffer[cnt]=dat[cnt];
		l_print(buffer,line_ctl,Left);
		if(line_ctl==5)
			line_ctl=0;
		else
			line_ctl++;
	}
	else
	{
		while(length-cnt>14)
		{
			memcpy(buffer,dat+cnt,14);
			l_print((char*)&"              ",line_ctl,Left);
			l_print(buffer,line_ctl,Left);
			if(line_ctl==5)
				line_ctl=0;
			else
				line_ctl++;
			memset(buffer,0,sizeof(buffer));
			cnt+=14;
		}
		memcpy(buffer,dat+cnt,length-cnt);
		uint8_t t_buffer[14];
		memset(t_buffer,' ',sizeof(t_buffer));
		for(cnt=0;cnt<length;cnt++)
		{
			if(buffer[cnt])
				t_buffer[cnt]=buffer[cnt];
		}
		l_print((char*)t_buffer,line_ctl,Left);
		if(line_ctl==5)
				line_ctl=0;
		else
			line_ctl++;
	}
}

void l_print(const char* dat,uint8_t line,enum StrLocation loc)
{
	//14 chars
	uint8_t length=strlen((const char*)dat),i;
	if(loc!=Right){
		if(loc==Middle)
		{
			SetLine(line);
			for(i=0;i<(14-length)/2;i++) g_puts(' ');
		}
		else if(loc==Left) SetLine(line);
		g_print(dat);
	}
	else{
		//char temp[15]="              ";
		//uint8_t start=14-length;
		//for(i=0;i<length;i++){
		//	temp[start+i]=dat[i];
		//}
		//SetLine(line);
		//g_print(temp);
		LCD_SetXY((14-length)*6,line);
		g_print(dat);
	}
}

