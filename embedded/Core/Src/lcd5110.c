void g_print(char* dat)
{
	char* ptr=dat;
	while(*ptr!=0)
	{
		g_puts(*ptr);
		ptr++;
	}
}

void SetLine(uint8_t line)
{
	g_line=line;
//	LCD_SetXY(83,5-line);
	LCD_SetXY(0,line);
}

