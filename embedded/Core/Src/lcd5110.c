#include "lcd5110.h"
uint8_t g_line=0;
uint8_t g_x=0;
void LCD_WriteByte(uint8_t dc,uint8_t b)
{
	uint8_t i;
	SCE_H;
	SCE_L;
	if(dc) DC_H;
	else DC_L;
	for(i=0;i<8;i++)
	{
		//10101010
		SCLK_L;
		if(b&0x80)
		DIN_H;
		else DIN_L;
		SCLK_H;
		b=b<<1;
		//delay_us(50);
	}
	SCLK_L;
	DIN_L;
	SCE_H;
	//delay_us(100);
}

void LCD_Init(void)
{
	SCE_H;
	RST_L;
	HAL_Delay(10);
	RST_H;
	LCD_WriteByte(CMD,0x21);
  LCD_WriteByte(CMD,0xc8);
  LCD_WriteByte(CMD,0x06);
  LCD_WriteByte(CMD,0x13);
  LCD_WriteByte(CMD,0x20);
  LCD_Clean();
  LCD_WriteByte(CMD,0x0c);
	SCE_L;
}

void LCD_SetXY(uint8_t x,uint8_t y)
{
	g_x=x;g_line=y;
//	x=83-x;
//	y=5-y;
	x+=0x80;
	y+=0x40;

	LCD_WriteByte(CMD,x);
	LCD_WriteByte(CMD,y);
}

void LCD_Clean(void)
{
	uint16_t i=503;
	for(i=0;i<503;i++)
	{
		LCD_WriteByte(DAT,0x00);
	}
}

